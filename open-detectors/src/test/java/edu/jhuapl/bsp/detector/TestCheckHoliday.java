        dt2 = cal.getTime();
        cal.set(2010, 11, 31);
        dt3 = cal.getTime();
        int dataReal[], flags[];
//
        System.out.println("Testing CheckHoliday function");
        dataReal = v1;
        flags = CheckHoliday.isHoliday(dt1, 992);
        for (int i = 0; i < dataReal.length; i++) {
            assertEquals(dataReal[i], flags[i], .00001);
        }
        dataReal = v2;
        flags = CheckHoliday.isHoliday(dt2, 992);
